void main(List<String> args) {
  print("Operador &&");
  print("-----------");
  print("True && True = ${true && true}");
  print("True && False = ${true && false}");
  print("False && True = ${false && true}");
  print("False && False = ${false && false}");

  print("Operador ||");
  print("-----------");
  print("True || True = ${true || true}");
  print("True || False = ${true || false}");
  print("False || True = ${false || true}");
  print("False || False = ${false || false}");
}
